Instalação do Painel é simples
Todos os código é pelo config (Webhook, perm e tudo mais)
Para puxar todos os carros, dentro da sua vRP tem que ter o vRP.vehicleGlobal() onde ele retorna a lista de todos os seu veículos ai já irá aparecer no sistema
PAra puxar todos os itens, dentro da sua vRP tem quer ter o vRP.getAllItens() onde ele retorna a lista de todos os itens da sua base ai já irá aparecer